static void render_logo(void) {
    // Host Keyboard Layer Status
	void animation_phase(void) {
        current_idle_frame = (current_idle_frame + 1) % 2;
        oled_write_raw_P(frames[abs(1 - current_idle_frame)], sizeof(frames));
    }
	
	if (get_current_wpm() != 000) { // prevent sleep
		oled_on();
		if(timer_elapsed32(anim_timer) > (current_idle_frame == 1 ? 600 : 200)) { 
		    anim_timer = timer_read32();
		    animation_phase();
		}
		anim_sleep = timer_read32();
    } else { // turn off screen when timer threshold elapsed or reset time since last input
		if (timer_elapsed32(anim_sleep) > OLED_TIMEOUT) {
			oled_off();
		} else {
			if (timer_elapsed32(anim_timer) > (current_idle_frame == 1 ? 600 : 200)) {
				anim_timer = timer_read32();
				animation_phase();
			}
		}
    }
}

void oled_task_user(void) {
    render_logo();
}

void suspend_power_down_user(void) {
    oled_off();
}
